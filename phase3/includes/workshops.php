    <p>Web developers are enthusiastic about mobile, but need some background information as well as a chance to practice their skills on as many phones as possible. Therefore PPK will be running this workshop to teach you what you need to know.
  
    <p>We found that a one-day workshop is too short. By the time the first afternoon ends you've just started to understand what it's all about, and you're beginning to get some ideas for future experiments. Besides, you want to talk to your fellow attendees about practical problems and phones. Therefore our workshop is a two-day affair, and we'll also have dinner together at the end of day 1 (plus maybe a few beers, if you're so inclined).

    <p>We are not big believers in workshops where the teacher talks for eight hours straight. Therefore the idea is that you take a genuine project you have to work on for a genuine client, or, failing that, a mobile experiment you're working on. We'll discuss these practical examples, you'll work on them during the workshop, and we'll solve practical problems together.

    <p>We will take plenty of test phones, including a few obscure ones such as the Nokia N900 (Maemo), the HTC Smart (Brew MP), or a low-end LG Android, that we can try stuff on. If you have them you should also take a few phones, and not only iPhones or Androids. That allows you and your fellow attendees to test your websites on browsers and operating systems you've never even heard of. (Did we mention how exciting the mobile web is?)


    <h2>What you'll learn</h2>
    <ul>
      <li>General overview of the mobile (browser) landscape
      <li>Dealing with viewports and media queries
      <li>Using the touch events; differences with mouse events
      <li>Basics of mobile JavaScript (mainly peformance and memory management). To be honest, this point mostly consists of: "don't automatically use a library."
      <li>Mobile debugging
      <li>Anything else you want to learn
    </ul>

    <p>You are expected to be proficient in desktop web development. If you also do JavaScript, it helps if you're proficient in librariless JavaScript development, since for now libraries are counter-indicated for mobile web sites.

    

    <p>JavaScript is increasingly seen as the ubiquitous language for developing for the web, so it stands to reason that JavaScript be used for the server side too.

    <p>Node allows for applications to be developed in JavaScript and is running on the incredibly fast V8 engine (the same JavaScript engine used to run Google Chrome). It can handle thousands of concurrent connections due to it’s event model, and because of this event model, developing for Node is very familiar if you’ve already written JavaScript for the browser - as this the same event model.

